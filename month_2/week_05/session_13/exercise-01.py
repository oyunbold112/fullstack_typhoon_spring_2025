# Таны код энд:
# hello.txt файлын агуулгыг уншиж хэвлэх функцийг гүйцээнэ үү


def read_hello_file():
    # Кодоо энд бичнэ үү
    with open('hello.txt', 'r') as file:
        content = file.read()
        print(content)
        file.close()

# Функцийг дуудна
read_hello_file()

